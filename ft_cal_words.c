#include "libft.h"

int	ft_cal_words(char const *s, char c)
{
	int i;

	i = 0;
	if (*s && *s != c)
		i++;
	while (*s)
	{
		if (*s != c && *(s - 1) == c)
			i++;
		s++;
	}
	return (i);
}
